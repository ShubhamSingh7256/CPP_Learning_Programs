#include<iostream>
using namespace std;

int main()
{
    int a;
    char b;
    float c;
    double d;
    bool e;
    signed int f;
    unsigned int g;
    short int h;
    long int i;

    cout<<"size of int :"<<sizeof(a)<<endl;
    cout<<"size of char :"<<sizeof(b)<<endl;
    cout<<"size of float :"<<sizeof(c)<<endl;
    cout<<"size of double :"<<sizeof(d)<<endl;
    cout<<"size of bool :"<<sizeof(e)<<endl;
    cout<<"size of signed int :"<<sizeof(f)<<endl;
    cout<<"size of unsigned int :"<<sizeof(g)<<endl;
    cout<<"size of short int :"<<sizeof(h)<<endl;
    cout<<"size of long int :"<<sizeof(i)<<endl;



    return 0;
}